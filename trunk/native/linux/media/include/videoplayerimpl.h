#ifndef LWUIT_VIDEOPLAYERIMPL_H
#define LWUIT_VIDEOPLAYERIMPL_H

#include "player.h"

namespace jlwuit {

class VideoPlayerImpl : public jlwuit::Player {

	private:
		/** \brief */
		Control *_video_size;
		/** \brief */
		Control *_video_format;

	public:
		/**
		 * \brief
		 *
		 */
		VideoPlayerImpl();

		/**
		 * \brief
		 *
		 */
		virtual ~VideoPlayerImpl();

		/**
		 * \brief
		 *
		 */
		virtual void Play();

		/**
		 * \brief
		 *
		 */
		virtual void Pause();

		/**
		 * \brief
		 *
		 */
		virtual void Stop();

		/**
		 * \brief
		 *
		 */
		virtual void Resume();

		/**
		 * \brief
		 *
		 */
		virtual void Close();

		/**
		 * \brief
		 *
		 */
		virtual void SetMediaTime(uint64_t i);

		/**
		 * \brief
		 *
		 */
		virtual uint64_t GetMediaTime();

		/**
		 * \brief
		 *
		 */
		virtual void SetLoop(bool b);

		/**
		 * \brief
		 *
		 */
		virtual bool IsPlaying();
		
		/**
		 * \brief
		 *
		 */
		virtual bool IsLoop();
		
		/**
		 * \brief
		 *
		 */
		virtual double GetDecodeRate();

		/**
		 * \brief
		 *
		 */
		virtual void SetDecodeRate(double rate);

};

}

#endif
